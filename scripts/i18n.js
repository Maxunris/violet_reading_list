export function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : DEFAULT_LANGUAGE;
}

export function getAvailableLanguages() {
  return [
    { id: 'en', label: messages.en.languageEnglish },
    { id: 'ru', label: messages.ru.languageRussian }
  ];
}

export function t(language, key, params = {}) {
  const resolvedLanguage = normalizeLanguage(language);
  const bundle = messages[resolvedLanguage] ?? messages[DEFAULT_LANGUAGE];
  const template = bundle[key] ?? messages[DEFAULT_LANGUAGE][key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ''));
}
